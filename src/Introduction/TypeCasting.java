package Introduction;

public class TypeCasting {
    public static void main(String[] args) {
        int in;
        char ch='a';
        in =ch;
        System.out.println(ch);
        System.out.println(in);
        System.out.println((int) ch);
        ch=70;
        System.out.println(ch);
        System.out.println((int)200);

        System.out.println((int) ' ');
    }
}
